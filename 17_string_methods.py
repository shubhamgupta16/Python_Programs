# string methods part one

name = "ShUbHaM gUPta"

# 1. len() function
print(len(name))
print(len("Shubham"))
# or
length = len(name)
print(length)

# 2. lower() method
print(name.lower())

# 3. upper() method
print(name.upper())

# 4. title() method
print(name.title())

# 5. count() method
print(name.count("h")) # output is 1 because small h is one

